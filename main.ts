controller.B.onEvent(ControllerButtonEvent.Pressed, function () {
    if (mySprite.tileKindAt(TileDirection.Top, sprites.dungeon.purpleSwitchUp)) {
        if (sword_not_recieved) {
            music.play(music.melodyPlayable(music.buzzer), music.PlaybackMode.InBackground)
            game.showLongText("Maybe I should see what's in that chest first", DialogLayout.Bottom)
        }
        if (Sword_recieved) {
            tiles.setCurrentTilemap(tilemap`level1`)
            music.play(music.melodyPlayable(music.baDing), music.PlaybackMode.InBackground)
            game.showLongText("You opened the door!", DialogLayout.Bottom)
        }
    }
    if (!(third_room)) {
        if (mySprite.tileKindAt(TileDirection.Left, sprites.dungeon.chestClosed) || mySprite.tileKindAt(TileDirection.Right, sprites.dungeon.chestClosed) || (mySprite.tileKindAt(TileDirection.Top, sprites.dungeon.chestClosed) || mySprite.tileKindAt(TileDirection.Bottom, sprites.dungeon.chestClosed))) {
            tiles.setCurrentTilemap(tilemap`level0`)
            game.setDialogTextColor(5)
            music.play(music.melodyPlayable(music.powerUp), music.PlaybackMode.InBackground)
            game.showLongText("You found an old sword and shield!", DialogLayout.Bottom)
            game.setDialogTextColor(15)
            Sword_recieved = true
            sword_not_recieved = false
        }
    }
    if (third_room) {
        if (mySprite.tileKindAt(TileDirection.Left, sprites.dungeon.chestClosed) || mySprite.tileKindAt(TileDirection.Right, sprites.dungeon.chestClosed) || (mySprite.tileKindAt(TileDirection.Top, sprites.dungeon.chestClosed) || mySprite.tileKindAt(TileDirection.Bottom, sprites.dungeon.chestClosed))) {
            tiles.setCurrentTilemap(tilemap`level10`)
            game.setDialogTextColor(5)
            music.play(music.melodyPlayable(music.powerUp), music.PlaybackMode.InBackground)
            game.showLongText("You found a manual on how to jump maybe by pressing B I can jump across that gap", DialogLayout.Bottom)
            game.setDialogTextColor(15)
            jump_acquired = true
        }
    }
    if (second_room) {
        if (jump_acquired) {
            if (jumpcan) {
                game.showLongText("Here goes nothing if I fall ill probably die..", DialogLayout.Bottom)
                animation.runMovementAnimation(
                mySprite,
                animation.animationPresets(animation.easeRight),
                2000,
                false
                )
                pause(2000)
                made_the_jump = true
                if (made_the_jump) {
                    game.showLongText("Phew almost thought I fell there!", DialogLayout.Bottom)
                }
                if (mySprite.tileKindAt(TileDirection.Center, assets.tile`myTile5`)) {
                    game.gameOver(false)
                    game.setGameOverEffect(false, effects.dissolve)
                    game.setGameOverPlayable(false, music.melodyPlayable(music.wawawawaa), false)
                    pause(5000)
                }
                if (mySprite.tileKindAt(TileDirection.Center, sprites.dungeon.hazardHole)) {
                    game.gameOver(false)
                    game.setGameOverEffect(false, effects.dissolve)
                    game.setGameOverPlayable(false, music.melodyPlayable(music.wawawawaa), false)
                    pause(5000)
                }
            }
        }
    }
    if (fourth_room) {
        if (mySprite.tileKindAt(TileDirection.Left, sprites.dungeon.chestClosed) || mySprite.tileKindAt(TileDirection.Right, sprites.dungeon.chestClosed) || (mySprite.tileKindAt(TileDirection.Top, sprites.dungeon.chestClosed) || mySprite.tileKindAt(TileDirection.Bottom, sprites.dungeon.chestClosed))) {
            tiles.setTileAt(tiles.getTileLocation(2, 5), sprites.dungeon.chestOpen)
            game.setDialogTextColor(5)
            music.play(music.melodyPlayable(music.powerUp), music.PlaybackMode.InBackground)
            game.showLongText("You found a key!", DialogLayout.Bottom)
            game.setDialogTextColor(15)
            Keyfound = true
        }
    }
    if (second_room) {
        if (mySprite.tileKindAt(TileDirection.Top, sprites.dungeon.doorLockedNorth)) {
            tiles.setTileAt(tiles.getTileLocation(2, 5), sprites.dungeon.doorOpenNorth)
            game.setDialogTextColor(5)
            music.play(music.melodyPlayable(music.baDing), music.PlaybackMode.InBackground)
            game.showLongText("You opened the door!", DialogLayout.Bottom)
            game.setDialogTextColor(15)
            door_unlocked = true
            Keyfound = false
        }
    }
})
